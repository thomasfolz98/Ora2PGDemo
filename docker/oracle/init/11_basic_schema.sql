-- Schema APP_BASIC_DEMO: Produkte + Bestellungen
-- Alle Oracle-Typen werden von Ora2Pg 1:1 ohne Patch nach PostgreSQL konvertiert.

ALTER SESSION SET CONTAINER = XEPDB1;

CREATE TABLE APP_BASIC_DEMO.PRODUKTE (
    ID           NUMBER GENERATED BY DEFAULT AS IDENTITY
                   CONSTRAINT pk_produkte PRIMARY KEY,
    NAME         VARCHAR2(100)  NOT NULL,
    BESCHREIBUNG VARCHAR2(500),
    PREIS        NUMBER(10,2)   NOT NULL,
    LAGERBESTAND NUMBER(10)     DEFAULT 0 NOT NULL,
    ERSTELLT_AM  DATE           DEFAULT SYSDATE NOT NULL
);

-- Einfacher Index auf NAME -- migriert automatisch
CREATE INDEX IDX_PRODUKTE_NAME ON APP_BASIC_DEMO.PRODUKTE (NAME);

COMMENT ON TABLE  APP_BASIC_DEMO.PRODUKTE           IS 'Produktkatalog';
COMMENT ON COLUMN APP_BASIC_DEMO.PRODUKTE.PREIS     IS 'Netto-Verkaufspreis in EUR';
COMMENT ON COLUMN APP_BASIC_DEMO.PRODUKTE.LAGERBESTAND IS 'Aktueller Lagerbestand in Stueck';

CREATE TABLE APP_BASIC_DEMO.BESTELLUNGEN (
    ID              NUMBER GENERATED BY DEFAULT AS IDENTITY
                      CONSTRAINT pk_bestellungen PRIMARY KEY,
    PRODUKT_ID      NUMBER     NOT NULL
                      CONSTRAINT fk_best_produkt
                        REFERENCES APP_BASIC_DEMO.PRODUKTE(ID),
    MENGE           NUMBER(10) NOT NULL,
    BESTELLDATUM    DATE       DEFAULT SYSDATE NOT NULL,
    AKTUALISIERT_AM DATE
);

CREATE INDEX IDX_BEST_PRODUKT ON APP_BASIC_DEMO.BESTELLUNGEN (PRODUKT_ID);

COMMENT ON TABLE  APP_BASIC_DEMO.BESTELLUNGEN               IS 'Produktbestellungen';
COMMENT ON COLUMN APP_BASIC_DEMO.BESTELLUNGEN.AKTUALISIERT_AM IS 'Wird durch Trigger TRG_BESTELLUNG_UPDATE gesetzt';

-- Einfacher Row-Level-Trigger -- migriert automatisch via Ora2Pg
CREATE OR REPLACE TRIGGER APP_BASIC_DEMO.TRG_BESTELLUNG_UPDATE
  BEFORE UPDATE ON APP_BASIC_DEMO.BESTELLUNGEN
  FOR EACH ROW
BEGIN
  :NEW.AKTUALISIERT_AM := SYSDATE;
END;
/

EXIT;
